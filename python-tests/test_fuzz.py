from thefuzz import fuzz
from thefuzz import process

print("="*60)
print("🛠️ เริ่มการทดสอบ TheFuzz ")
print("="*60)

# ---------------------------------------------------------
# Mock ฐานข้อมูลแคตตาล็อก (ใส่ชื่อที่คล้ายกันเพื่อหลอกและทดสอบ AI)
# ---------------------------------------------------------
db_catalog = [
    # กลุ่มสกรู (ต่างกันแค่ เบอร์ กับ ตัวอักษรหัว)
    "สกรูเกลียวปล่อย หัว F เบอร์ 7",
    "สกรูเกลียวปล่อย หัว F เบอร์ 8",
    "สกรูเกลียวปล่อย หัว P เบอร์ 7",
    "สกรูปลายสว่าน หัว F เบอร์ 7",
    
    # กลุ่มท่อ PVC (ต่างกันที่ ชั้นความหนา และ ขนาด)
    "ท่อ PVC ชั้น 8.5 ขนาด 2 นิ้ว",
    "ท่อ PVC ชั้น 13.5 ขนาด 2 นิ้ว",
    "ท่อ PVC ชั้น 8.5 ขนาด 1 นิ้ว",
    
    # กลุ่มสายไฟ (ต่างกันที่ ชนิดสาย, ขนาด, และ สี)
    "สายไฟ THW 1x2.5 Sq.mm. สีดำ",
    "สายไฟ THW 1x2.5 Sq.mm. สีแดง",
    "สายไฟ VAF 2x2.5 Sq.mm. สีขาว",
    
    # กลุ่มเทปและปูน
    "เทปพันสายไฟ 3M สีดำ",
    "เทปพันละลาย 3M สีดำ",
    "เทปกาว 2 หน้า 3M",
    "ปูนซีเมนต์ปอร์ตแลนด์ ตราช้าง",
    "ปูนซีเมนต์ผสม ตราเสือ"
]

# -------------------------------------------------------------------------------
# ข้อมูลที่ Mock ซึ่งข้อมูลพวกนี้จะมาจากข้อมูลที่ Uesr ส่งและสกัดมา (พิมพ์ย่อ, สลับคำ, เปลี่ยนภาษา)
# -------------------------------------------------------------------------------
test_cases = [
    "สกรูปล่อยเบอร์ 7",          # 1. พิมพ์ย่อ ขาดคำว่า "เกลียว" และไม่บอก "หัว F หรือ P"
    "ท่อพีวีซี 2 นิ้ว 8.5",        # 2. แปลง PVC เป็นภาษาไทย + สลับตำแหน่งคำ
    "สายไฟ thw ดำ 2.5",         # 3. พิมพ์ตัวเล็ก + ตัด Sq.mm. ออก
    "เทปดำ 3m",                 # 4. คลุมเครือ (เป็นเทปพันสายไฟ หรือ เทปพันละลาย?)
    "สกรูสว่าน #7",             # 5. ใช้สัญลักษณ์ # แทนคำว่าเบอร์
    "สีทาบ้าน สีขาว 1 ถัง"         # 6. ของชิ้นใหม่ที่ไม่มีในระบบเลย
]

# เราจะใช้ scorer แบบ token_sort_ratio เพราะเหมาะกับการพิมพ์สลับตำแหน่งมากที่สุด
scorer_type = fuzz.token_sort_ratio

for test_word in test_cases:
    # ป้องกันกรณีมีช่องว่างหรือการขึ้นบรรทัดใหม่ติดมาด้วย
    clean_word = test_word.strip()
    print(f"\n🔎 เปรียบเทียบข้อมูลเมื่อเจอคำว่า : [ {clean_word} ]")
    
    # สั่งให้ดึงตัวที่เหมือนที่สุด 3 อันดับแรกมาดู 
    matches = process.extract(clean_word, db_catalog, limit=3, scorer=scorer_type)
    
    # ดึงค่าคะแนนและชื่อของอันดับ 1 และอันดับ 2 ออกมาเพื่อเปรียบเทียบ
    top1_name, top1_score = matches[0][0], matches[0][1]
    top2_name, top2_score = matches[1][0], matches[1][1]
    
    print(f"   อันดับ 1: '{top1_name}' (ความมั่นใจ {top1_score}%)")
    print(f"   อันดับ 2: '{top2_name}' ({top2_score}%)")
    print(f"   อันดับ 3: '{matches[2][0]}' ({matches[2][1]}%)")
    
    # ==========================================
    # 🧠 ส่วนระบบประมวลผลการตัดสินใจ (Decision Logic)
    # ==========================================
    print("👉   สรุปการทำงานของระบบ:")
    
    if top1_score < 50:
        # ถ้าคะแนนต่ำกว่า 50 ตีตกไปเลย ถือว่าหาไม่เจอชัวร์ๆ
        print(f"❌   ไม่พบรายการที่ใกล้เคียงในระบบ -> [แจ้งให้เพิ่มเป็นวัสดุรายการใหม่]")
        
    elif top1_score == top2_score:
        # ถ้าคะแนนเกิน 50 แล้วบังเอิญเท่ากัน ค่อยเตือนว่าคลุมเครือ
        print(f"⚠️   ข้อมูลคลุมเครือ! AI แยกไม่ออกระหว่าง '{top1_name}' กับ '{top2_name}' -> [รอ User ยืนยัน]")
        
    elif top1_score >= 70:
        # คะแนนสูงและทิ้งห่างอันดับ 2 ถือว่ามั่นใจ
        print(f"✅    มั่นใจ! ระบบทำการจับคู่เป็น '{top1_name}'")
        
    else:
        # ที่เหลือคือคะแนน 50-69% (ค่อนข้างคล้ายแต่ไม่ชัวร์)
        print(f"🤔    ค่อนข้างคล้าย '{top1_name}' แต่คะแนนต่ำไป ({top1_score}%) -> [รอ User ยืนยัน]")
        
    print("-" * 60)